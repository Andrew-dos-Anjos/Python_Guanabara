#Comparando números
#----------

num1 = int(input("Digite um número:"))
num2 = int(input("Digite outro número:"))

if num1 > num2:
    print(f"O N°:{num1} é maior.")
if num1 < num2:
    print(f"O N°:{num2} é maior.")
if num1 == num2:
    print("Os dois números são iguais.")

#O código escrito dessa forma é para demonstrar que, numa situação simples*, também é válido.
