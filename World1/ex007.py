# Média aritmética
# ----------

num1 = float(input("Digite um número: "))
num2 = float(input("Digite um outro número: "))
media = (num1 + num2) / 2
#/X; se hovessem 5 num por ex seria /5.
print(f"A média entre {num1} e {num2} é = {media:.1f}")

#Dentro de {} pode ser colocado ":.Xf" para definir a quantidade de casas decimais.
